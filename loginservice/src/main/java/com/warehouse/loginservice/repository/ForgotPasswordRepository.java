package com.warehouse.loginservice.repository;

import com.warehouse.loginservice.entity.ForgotPassword;
import com.warehouse.loginservice.entity.User;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.Query;
import org.springframework.stereotype.Repository;

import java.util.Date;
import java.util.List;
import java.util.Optional;

@Repository
public interface ForgotPasswordRepository extends JpaRepository<ForgotPassword, Integer> {

    @Query("select fp from ForgotPassword fp where fp.otp = ?1 and fp.user = ?2")
    Optional<ForgotPassword> findByOtpAndUser(Integer otp, User user);

    List<ForgotPassword> findByUserAndExpirationTimeBefore(User user, Date expirationTime);

}
